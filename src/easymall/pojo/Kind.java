package easymall.pojo;

public class Kind {
	private Integer id;
	private Integer knum;
	private String name;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getKnum() {
		return knum;
	}
	public void setKnum(Integer knum) {
		this.knum = knum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
